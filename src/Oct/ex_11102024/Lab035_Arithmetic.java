package Oct.ex_11102024;

public class Lab035_Arithmetic {
    public static void main(String[] args) {
        // Arithmetic Operators
//        + (Addition)
//                - (Subtraction)
//                * (Multiplication)
//                / (Division)
//                % (Modulo) | Modulus - Remainder
        int a = 20;
        int b = 10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        //

//      10  | 20| 2 - Q
//          | 20|---
//              ----
//          0  -> R
    }
}
