package Oct.ex_21102024_ForLoop;

public class Lab092 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {    //10
            System.out.println(i);
            //i -> 0, 9 - 10 times
        }

        for (int j = 1; j <= 10; j++) {    //10
            System.out.println(j);
            // j -> 1 to 10, -> 10 times
        }

    }
}
