package Oct.ex_21102024_ForLoop;

public class Lab102 {
    public static void main(String[] args) {
        for (int i = 0; i < 10 ; i++) { // 0 to 9
            System.out.println("Sonal");
        }
        for (int i = 1; i <= 10 ; i++) { // 1 to 10
            System.out.println("Patel");
        }
        // | i=1 , 1 |
        // | i=2 , 2 |
        // | i=3 , 3 |
        // | i=4 , 4 |
        // | i=5 , 5 |
        // | i=6 , 6 |
        // | i=7 , 7 |
        // | i=8 , 8 |
        // | i=9 , 9 |
        // | i=10 , 10 |

    }
}
