package Oct.ex_21102024_ForLoop;

public class Lab103_Interview {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            if (i == 5) {
                System.out.println("Five");
            } else {
                System.out.println(i);
            }
        }
        //Output-
        //0....Five....9

    }
}
